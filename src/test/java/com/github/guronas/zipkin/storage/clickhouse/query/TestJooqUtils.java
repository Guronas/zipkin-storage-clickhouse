package com.github.guronas.zipkin.storage.clickhouse.query;

import org.jooq.DSLContext;
import org.jooq.SQLDialect;
import org.jooq.impl.DSL;
import org.jooq.tools.jdbc.MockConnection;
import org.jooq.tools.jdbc.MockDataProvider;

public class TestJooqUtils {

    public static DSLContext getTestDSLContext(MockDataProvider mockDataProvider) {
        MockConnection mockConnection = new MockConnection(mockDataProvider);
        //We pretend here that ClickHouse is just MySQL DB
        return DSL.using(mockConnection, SQLDialect.MYSQL);
    }
}
